document.addEventListener('DOMContentLoaded', function () {
    const wrapper = document.getElementById('passport-files-wrapper');
    const addBtn = document.getElementById('add-passport-file');
    let index = 1;

    addBtn.addEventListener('click', function () {
        const fileGroup = document.createElement('div');
        fileGroup.classList.add('custom-file');
        fileGroup.innerHTML = `
            <label class="custom-file-label" for="passportCopyDependants_${index}">Passport Copy (dependants)</label>
            <input type="file" class="custom-file-input" name="passportCopyDependants[]" id="passportCopyDependants_${index}">
            <span class="file-path"></span>
            <button type="button" class="remove-file-btn">Remove</button>
        `;
        wrapper.appendChild(fileGroup);
        index++;
    });

    // Event delegation for remove buttons
    wrapper.addEventListener('click', function (e) {
        if (e.target && e.target.classList.contains('remove-file-btn')) {
            e.target.parentElement.remove();
        }
    });
});


